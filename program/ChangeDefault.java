public class ChangeDefault {
	static boolean val1 = true;
	static double val2 = 10.0;
	static float val3 = 2.50f;
	static int val4 = 9;
	static long val5 = 1233;
	static String val6 = "Java";
	static byte b = 1;
	static short s = 2;
	static char ch = 'Y';
	public static void main(String[] args) {
		System.out.println(" Modified Values....");
		System.out.println(" Boolean = " + val1);
		System.out.println(" Double = " + val2);
		System.out.println(" Float = " + val3);
		System.out.println(" Integer = " + val4);
		System.out.println(" Long = " + val5);
		System.out.println(" String = " + val6);
		System.out.println(" byte = " + b);
		System.out.println(" Short = " + s);
		System.out.println(" Char = " + ch);
	}
}