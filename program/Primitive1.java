public class Primitive1 {
	boolean val1;
	double val2;
	float val3;
	int val4;
	long val5;
	String val6;
	byte b;
	short s;
	char ch;
	public static void main(String[] args) {
		Primitive1 p = new Primitive1();
		System.out.println(" Default Values....");
		System.out.println(" Boolean = " + p.val1);
		System.out.println(" Double = " + p.val2);
		System.out.println(" Float = " + p.val3);
		System.out.println(" Integer = " + p.val4);
		System.out.println(" Long = " + p.val5);
		System.out.println(" String = " + p.val6);
		System.out.println(" byte = " + p.b);
		System.out.println(" Short = " + p.s);
		System.out.println(" Char = " + p.ch);
	}
}